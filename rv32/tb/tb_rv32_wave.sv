  wire [31:0] imem_addr;
  reg         imem_ready;
  reg         imem_rdata_valid;
  reg  [31:0] imem_rdata;

  wire        dmem_valid;
  wire        dmem_we;
  wire [3:0]  dmem_wstrb;
  wire [31:0] dmem_addr;
  wire [31:0] dmem_wdata;
  reg         dmem_ready;
  reg         dmem_rdata_valid;
  reg  [31:0] dmem_rdata;

  rv32_top dut (
    .clk              (clk),
    .rst_n            (rst_n),
    .imem_valid       (imem_valid),
    .imem_addr        (imem_addr),
    .imem_ready       (imem_ready),
    .imem_rdata_valid (imem_rdata_valid),
    .imem_rdata       (imem_rdata),
    .dmem_valid       (dmem_valid),
    .dmem_we          (dmem_we),
    .dmem_wstrb       (dmem_wstrb),
    .dmem_addr        (dmem_addr),
    .dmem_wdata       (dmem_wdata),
    .dmem_ready       (dmem_ready),
    .dmem_rdata_valid (dmem_rdata_valid),
    .dmem_rdata       (dmem_rdata)
  );

  // -------------------------------------------------------------------
  // Instruction ROM (1 KiB) and data RAM (1 KiB)
  // -------------------------------------------------------------------
  reg [31:0] rom [0:1023];
  reg [31:0] ram [0:255];
  integer    i;

  // -------------------------------------------------------------------
  // Zero-wait memory model
  // -------------------------------------------------------------------
  always @(*) begin
    imem_ready       = 1'b1;
    imem_rdata_valid = rst_n && imem_valid;
    imem_rdata       = rom[imem_addr[11:2]];
  end

  always @(*) begin
    dmem_ready       = 1'b1;
    dmem_rdata_valid = rst_n && dmem_valid & ~dmem_we;
    dmem_rdata       = ram[dmem_addr[9:2]];
  end

  always @(posedge clk) begin
    if (dmem_valid && dmem_we) begin : ram_write_blk
      integer   wi;
      reg [31:0] cur;
      wi  = dmem_addr[9:2];
      cur = ram[wi];
      if (dmem_wstrb[0]) cur[7:0]   = dmem_wdata[7:0];
      if (dmem_wstrb[1]) cur[15:8]  = dmem_wdata[15:8];
      if (dmem_wstrb[2]) cur[23:16] = dmem_wdata[23:16];
      if (dmem_wstrb[3]) cur[31:24] = dmem_wdata[31:24];
      ram[wi] <= cur;
    end
  end

  // -------------------------------------------------------------------
  // Waveform visibility: test_id and test_done
  // -------------------------------------------------------------------
  integer    test_id;
  reg        test_done;
  reg [31:0] last_ram1;

  initial begin
    test_id   = 0;
    test_done = 1'b0;
    last_ram1 = 32'h0;
  end

  always @(posedge clk) begin
    test_done <= 1'b0;
    if (ram[1] !== last_ram1) begin
      last_ram1 <= ram[1];
      if (ram[1] > 0 && ram[1] <= 37) begin
        test_id   <= ram[1];
        test_done <= 1'b1;
      end
    end
  end

