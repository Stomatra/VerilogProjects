  // Clock / reset
  // -------------------------------------------------------------------
  reg clk   = 1'b0;
  reg rst_n = 1'b0;
  always #5 clk = ~clk; // 100 MHz, 10 ns period

  // -------------------------------------------------------------------
  // DUT <-> TB connections
  // -------------------------------------------------------------------
