// ============================================================
// tb_rv32_wave
// ------------------------------------------------------------
// Vivado/xsim-friendly testbench for RV32I 5-stage pipeline.
//
// Purpose:
//   - Single-run waveform demo of all 37 standard RV32I instructions.
//   - No external hex/python files required: ROM is embedded inline.
//   - Exposes test_id (1..37) and test_done pulse for waveform inspection.
//
// Memory convention:
//   ram[1] (byte addr 0x04): progress marker (test_id 1..37) or 0xDEADBEEF on fail.
//   ram[2] (byte addr 0x08): set to 1 when all 37 tests pass.
//
// Signals to add in Vivado waveform window:
//   tb_rv32_wave.test_id          -- current test number (1..37)
//   tb_rv32_wave.test_done        -- 1-cycle pulse each time test_id advances
//   tb_rv32_wave.dut.imem_addr    -- fetch PC
//   tb_rv32_wave.dut.imem_rdata   -- fetched instruction word
//   tb_rv32_wave.dut.u_core.pc_q  -- PC register (pipeline IF stage)
//   tb_rv32_wave.dut.dmem_valid   -- data memory access enable
//   tb_rv32_wave.dut.dmem_we      -- 1=store, 0=load
//   tb_rv32_wave.dut.dmem_addr    -- data address
//   tb_rv32_wave.dut.dmem_wdata   -- store data
//   tb_rv32_wave.dut.dmem_rdata   -- load data (from RAM)
//
// Vivado usage:
//   1. Add rv32/rtl/*.sv and rv32/tb/tb_rv32_wave.sv to project sources.
//   2. Set tb_rv32_wave as simulation top.
//   3. Run Simulation -> Run All (or 'run 200us' in Tcl console).
//   4. Add the signals above to the waveform window.
//   Use test_done pulses as natural segment markers between tests.
//
// PASS/FAIL output:
//   [TB] PASS  -- all 37 tests completed successfully.
//   [TB] FAIL  -- a test failed (ram[1] == 0xDEADBEEF).
//   [TB] TIMEOUT -- simulation exceeded MAX_CYCLES without a result.
// ============================================================
module tb_rv32_wave;

